            @ Process vs Thread
                  % Process :- Running program (EXE) Has its own memory space.
                  % Thread  :- Smallest unit of execution inside a process Shares memory with
                               other threads of same process
            NOTE :-  One process can have many threads.

            @ Single Threading vs Multithreading :-
